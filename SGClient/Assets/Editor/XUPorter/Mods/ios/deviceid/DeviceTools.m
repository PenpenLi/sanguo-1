+ (NSString*)deviceDesc
{
    NSString *str = [NSString stringWithFormat:@"1|%@|%@|%d*%d|%@|%@ %@|%@|%@",
                     [DeviceTools macaddress],
                     [[[ASIdentifierManager sharedManager] advertisingIdentifier] UUIDString],
                     [DeviceTools heightForScreen],[DeviceTools widthForScreen],
                     [[UIDevice currentDevice] name],
                     [[UIDevice currentDevice] systemName],[[UIDevice currentDevice] systemVersion],
                     [DeviceTools networktype],
                     [DeviceTools getcarrierName]];
    return str;
}

